'''
to map with real world scenarios we started using objects in code 
this is called object oriented programming 
class is a blueprint for creating objects 
'''
# this is a class 
class Student :
    name = "jitendra"
    lastName = "lowanshi"

# this is an object 
obj = Student()
print(obj.name)
print(obj.lastName)

# another example 
class Car:
    color = "blue"
    brand = "tata"

car1 = Car()
print(car1.color)
print(car1.brand)
